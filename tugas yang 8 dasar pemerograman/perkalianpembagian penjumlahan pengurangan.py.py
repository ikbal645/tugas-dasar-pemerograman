angka1 = int(input('Masukkan angka yang diinginkan: '))
angka2 = int(input('Masukkan angka yang diinginkan: '))

# Perkalian
perkalian = angka1 * angka2
print('Hasil perkalian:', perkalian)

# Pembagian
if angka2 != 0:  
    pembagian = angka1 / angka2
    print('Hasil pembagian:', pembagian)
else:
    print('Pembagian dengan nol tidak diperbolehkan.')

# Penjumlahan
penjumlahan = angka1 + angka2
print('Hasil penjumlahan:', penjumlahan)

# Pengurangan
pengurangan = angka1 - angka2
print('Hasil pengurangan:', pengurangan)